const createNewWindow = document.getElementById('create-new-window');
const closeNewWindow = document.getElementById('close-new-window');
// createNewWindow.addEventListener('click', () => {
//   window.open('https://www.youtube.com/watch?v=Ob4Nk6gz8Ec', '_blank');
// });
let electronWindow;
createNewWindow.addEventListener('click', () => {
  electronWindow = window.open(
    'https://www.youtube.com/watch?v=Ob4Nk6gz8Ec',
    '_blank',
  );
});

closeNewWindow.addEventListener('click', () => {
  if (electronWindow !== null) {
    electronWindow.close();
  }
});
